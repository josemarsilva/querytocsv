SELECT OWNER, 
       TABLE_NAME, 
       123456789012345 AS NUM_FLOAT, 
       3.1415926535 AS NUM_DOUBLEPRECISION, 
       1234567.89 AS NUM_MONEY,
       NULL AS NULL_VALUE,
       TRUNC(SYSDATE) AS DATE_DDMMYYYY,
       SYSDATE AS DATETIME_DDMMYYYHHMISS
FROM   ALL_TABLES 
WHERE  OWNER NOT IN ( 'SYS', 'SYSTEM', 'XDB' ) 
AND    ROWNUM < 1000 
ORDER  BY OWNER, TABLE_NAME
